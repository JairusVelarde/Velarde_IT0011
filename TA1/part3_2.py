# for letter b

#set first number to 1
loop = 1
print("FOR LETTER B")

#while loop to iterate every number
while loop <= 7:
    
    #makes the  increment by 2 stop after reaching 5
    if loop <= 4:
     print(str(loop) * loop)
     loop += 2
     
    #only adds 1 from numbers 5-7
    else:
     print(str(loop) * loop)
     loop += 1
    

